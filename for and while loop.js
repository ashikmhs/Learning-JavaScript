// while loop
var i = 1; // initialization

while ( i<=5 ) { //condition
    console.log(i);
    i++; //update
}

console.log("End of while loop!")
console.log("Start of for loop!");
// for loop

for (var j = 1; j<=5 ; j++) {
    console.log(j);
}

for (var k = 5; k >=1; k-- ) {
    console.log(k);
}